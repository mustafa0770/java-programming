package day04_variables_intro;

public class VariablesPractice {
    public static void main(String[] args){
        //declare variable students that stores number
        int students; //declare once
        students = 400; //assign 400
        System.out.println(students); //400
        System.out.println(students);
        students = 70;
        System.out.println(students);

        students = 444;
        students = 414;
        System.out.println(students);

        int teachers; //declare once
        teachers = 5;
        int mentors; //declare once
        mentors = 25;
        System.out.print("Number of teachers: ");
        System.out.println(teachers);

        System.out.print("Number of mentors: ");
        System.out.println(mentors);
        System.out.println(mentors);
    }

}
