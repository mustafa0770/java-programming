package day05_primitives_concatanation;

public class JobInfo {
    public static void main(String[] args){
        String title = "Java SoftwareDeveloper";
        String company = "Apple";
        String jobDescription = "Experience in Java, Selenium, Cucumber, JUnit";
        double salary = 120000;
        byte yearsOfExp = 3;
        boolean hasBenefits = true;

        System.out.println("Title:\t\t\t\t"+title);
        System.out.println("Company:\t\t\t"+company);
        System.out.println("JobDescription:\t\t"+jobDescription);
        System.out.println("Salary:\t\t\t\t$"+salary);
        System.out.println("Experience:\t\t\t"+yearsOfExp);
        System.out.println("Has Benefits:?\t\t"+hasBenefits);




    }

}
