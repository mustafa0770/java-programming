package day05_primitives_concatanation;

public class PrimitivesExample {
    public static void main(String[] args){
        //byte  > short > int > long
        //float > double
        //char
        //boolean
        byte byteValue = 127;
        System.out.println(byteValue);
        short shortValue = 32000;
        System.out.println(shortValue);
        int intValue = 1999999999;
        System.out.println(intValue);
        long longValue = 1915591989894584443L; //If your number is too long add L
        System.out.println(longValue);

        //*****Decimal Numbers/Floating Point types*****
        float floatValue = 2345.55F; //you can put F cuz of its high number
        System.out.println(floatValue);
        double doubleValue = 46905435.110; //D is optional because it's already doubled
        System.out.println(doubleValue);

        //********Character Type************
        char charValue = 'M'; //It has to be only one character numbers or letters
        System.out.println(charValue);

        //************Boolean True/False***************
        boolean booleanValue = true; //it has to only true/false with lower case
        System.out.println(booleanValue);











    }
}
