package day03_escape_sequence;

public class EscapeSequence2 {
    public static void main(String[] args){
        System.out.println("\"phone case\",\"Iphone\",\t\"xbox\",\n\t\"webcam\",\t\"monitor\",\n\t\t\"keyboard\"");
        System.out.println("       ~ ~ ~ ~");
        System.out.println("      ^");
        System.out.println("   ^ |-| ");
        System.out.println(" /   \\");
        System.out.println("|     |");
        System.out.println("-------");
    }
}
